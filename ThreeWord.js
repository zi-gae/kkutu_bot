setInterval(function isMyturn() {
  var get_isMyturn = document.getElementsByClassName("game-input")[0].style
    .display; //값 가져오기
  var Mode = document.getElementsByClassName("room-head-mode")[0].innerHTML;
  isRightmode = Mode.startsWith("한국어 쿵쿵따") ? true : false;
  if (get_isMyturn == "block" && isNextMyturn && isRightmode) {
    //block = isMyturn, isNextMyturn = 내턴일때 재실행 방지
    GetStartWord();
    console.log("[#] 쿵쿵따 핵 동작중");
    isNextMyturn = false;
    InputWrongWord = false;
  } else if (get_isMyturn == "none") {
    //none = NotMyturn
    isNextMyturn = true;
    FailText = null;
  }
}, delay);

function GetStartWord() {
  var get_firstWord = document.getElementsByClassName("jjo-display ellipse")[0]
    .innerHTML; //시작단어
  if (get_firstWord.includes("<")) get_firstWord = get_firstWord.split("<")[0];
  if (FailText != null && InputWrongWord) {
    get_firstWord = FailText[0];
    FailText = null;
    InputWrongWord = false;
  }
  var WordList = [];
  var Mission = document.getElementsByClassName("items")[0].innerHTML;
  var isTwoSoundWord = /^[가-힣][(][가-힣][)]$/.test(get_firstWord); //boolean
  if (isTwoSoundWord) {
    var FirstKeyword = get_firstWord.substring(0, 1);
    var SecondKeyword = get_firstWord.substring(2, 3);
    WordList = words.filter(
      (x) =>
        (x.startsWith(FirstKeyword) && !overlapwords.includes(x)) ||
        (x.startsWith(SecondKeyword) && !overlapwords.includes(x))
    );
  } else {
    WordList = words.filter(
      (x) => x.startsWith(get_firstWord) && !overlapwords.includes(x)
    );
  }
  WordList.sort((a, b) => b.split(Mission).length - a.split(Mission).length);
  if (WordList[0] != null) {
    SendWord(WordList[0]);
  } else if (!WordList[0]) {
    SendWord(get_firstWord + "..T.T");
  }
}

function SendWord(idx) {
  overlapwords.push(idx);
  document.querySelectorAll('[maxlength="' + MaxLength + '"]')[1].value = idx; //단어삽입
  document.querySelector("#ChatBtn").click(); //전송
}

setInterval(function CheckInputWrongWord() {
  FailText = document.getElementsByClassName("game-fail-text")[0].innerHTML;
  var isMT1 = document.getElementsByClassName("game-input")[0].style.display; //isMyTurn
  if (
    FailText != null &&
    isMT1 == "block" &&
    isRightmode &&
    !FailText.includes("..T.T")
  ) {
    if (
      FailText.startsWith("한방 단어") ||
      FailText.startsWith("젠틀") ||
      FailText.startsWith("이미 쓰인 단어")
    ) {
      FailText = FailText.split(":")[1].trim();
    }
    InputWrongWord = true;
    GetStartWord();
  }
}, 150);

