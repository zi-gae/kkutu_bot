function GetStartWord() {
  var get_firstWord = document.getElementsByClassName("jjo-display ellipse")[0]
    .innerHTML; //
  if (FailText != null && InputWrongWord) {
    get_firstWord = FailText[0];
    FailText = null;
    InputWrongWord = false;
  }
  var WordList = [];
  var Mission = document.getElementsByClassName("items")[0].innerHTML;
  var isTwoSoundWord = /^[-][(][-][)]$/.test(get_firstWord); //boolean
  if (isTwoSoundWord) {
    var FirstKeyword = get_firstWord.substring(0, 1);
    var SecondKeyword = get_firstWord.substring(2, 3);
    WordList = words.filter(
      (x) =>
        (x.startsWith(FirstKeyword) && !overlapwords.includes(x)) ||
        (x.startsWith(SecondKeyword) && !overlapwords.includes(x))
    );
  } else {
    WordList = words.filter(
      (x) => x.startsWith(get_firstWord) && !overlapwords.includes(x)
    );
  }
  WordList.sort((a, b) => b.length - a.length);
  WordList.sort((a, b) => b.split(Mission).length - a.split(Mission).length);

  if (WordList[0] != null) {
    SendWord(WordList[0]);
  } else if (!WordList[0]) {
    SendWord(get_firstWord + "..T.T");
  }
}

