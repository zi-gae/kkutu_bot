var delay = 100;
var isNextMyturn = false;
var CanResetHistoryWord = true;
var RoundSubject;
var overlapwords = [];
var FailText;
var isRightmode;
var isRightSubject;

setInterval(function isMyturn() {
  var get_isMyturn = document.getElementsByClassName("game-input")[0].style
    .display; //값 가져오기
  var Mode = document.getElementsByClassName("room-head-mode expl-mother")[0]
    .innerHTML;
  isRightmode = Mode.startsWith("한국어 단어 대결") ? true : false;
  if (
    get_isMyturn == "block" &&
    isNextMyturn &&
    isRightmode &&
    isRightSubject
  ) {
    //block = isMyturn, isNextMyturn = 내턴일때 재실행 방지
    GetStartWord();
    isNextMyturn = false;
  } else if (get_isMyturn == "none") {
    //none = NotMyturn
    isNextMyturn = true;
  }
}, delay);

function SendWord(idx) {
  overlapwords.push(idx);
  document.querySelectorAll('[maxlength="' + MaxLength + '"]')[1].value = idx; //단어삽입
}

function GetStartWord() {
  var list = subject[RoundSubject].filter((x) => !overlapwords.includes(x));
  var Mission = document.getElementsByClassName("items")[0].innerHTML;
  list.sort((a, b) => b.length - a.length);
  list.sort((a, b) => b.split(Mission).length - a.split(Mission).length);
  SendWord(list[0]);
}

setInterval(function isRoundEnd() {
  var Chain = document.getElementsByClassName("chain")[0].textContent;
  if (Chain == 0 && CanResetHistoryWord) {
    overlapwords = [];
    var title = document
      .getElementsByClassName("jjo-display ellipse")[0]
      .innerHTML.split("&lt;")[1]
      .split("&gt;")[0];
    if (subject[title] != null) {
      RoundSubject = title;
      isRightSubject = true;
    } else {
      isRightSubject = false;
    }
    CanResetHistoryWord = false;
  } else if (Chain > 0) {
    CanResetHistoryWord = true;
  }
}, 200);

setInterval(function gethistoryword() {
  var expl = document.getElementsByClassName(
    "ellipse history-item expl-mother"
  )[0].innerHTML;
  if (expl != null && expl.indexOf("<") != -1) {
    var v = expl.split("<")[0];
    if (!overlapwords.includes(v)) {
      overlapwords.push(v);
    }
  }
}, 100);

setInterval(function CheckInputWrongWord() {
  FailText = document.getElementsByClassName("game-fail-text")[0].innerHTML;
  var isMT1 = document.getElementsByClassName("game-input")[0].style.display; //isMyTurn
  if (
    FailText != null &&
    isMT1 == "block" &&
    isRightmode &&
    !FailText.includes("..T.T")
  ) {
    if (FailText.startsWith("다른 주제")) {
      var title = document
        .getElementsByClassName("jjo-display ellipse")[0]
        .innerHTML.split("&lt;")[1]
        .split("&gt;")[0];
      if (subject[title] == null) {
        isRightSubject = false;
      } else {
        isRightSubject = true;
      }
      RoundSubject = title;
    }
    GetStartWord();
  }
}, 150);
